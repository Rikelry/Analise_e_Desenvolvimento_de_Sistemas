// Desenvolva um programa que peça a nota de um aluno (0 a 10).
// Se a nota for maior ou igual a 7, mostre "Aprovado".
// Caso contrário, mostre "Reprovado".

let nota = Number(prompt("Digite a nota do aluno (0 a 10):"));

if (nota >= 7) {
  alert("Aprovado");
} else {
  alert("Reprovado");
}